def make_cal(event_id="test", family=EventFamily.CPI_HEADLINE, status=CalendarEventStatus.SCHEDULED):
    return CalendarEventRecord(
        calendar_event_id=event_id, release_family=family,
        scheduled_release_time=datetime(2024, 1, 1, 13, 30, 0),
        source_timezone="America/New_York", expected_components=[],
        release_authority="BLS", calendar_source="test",
        calendar_first_seen_at=datetime(2023, 12, 31, 12, 0, 0),
        status=status)

def make_exp(comp_id="headline_mom", exp_val=0.2, quality=ExpectationQuality.STRONG, captured_at=None):
    if captured_at is None:
        captured_at = datetime(2024, 1, 1, 8, 0, 0)
    return ExpectationSnapshot(
        expectation_snapshot_id="exp_"+comp_id, release_event_id="test_event",
        component_id=comp_id, captured_at=captured_at,
        valid_for_release_time=datetime(2024, 1, 1, 13, 30, 0),
        expected_value=exp_val, unit="percent", source_type="bloomberg",
        source_first_seen_at=captured_at, quality=quality)

def make_act(comp_id="headline_mom", act_val=0.1):
    return OfficialReleaseRecord(
        release_event_id="test_event", component_id=comp_id,
        actual_value=act_val, prior_value=0.2, unit="percent",
        published_at=datetime(2024, 1, 1, 13, 30, 0),
        first_seen_at=datetime(2024, 1, 1, 13, 30, 5),
        retrieved_at=datetime.utcnow(), source_id="bls",
        revision_status=RevisionStatus.INITIAL)

